# frozen_string_literal: true

module Sentiment
  class Lexicon
    POSITIVE = %w[
      bom ótima excelente satisfeito feliz engajado motivado crescimento
      aprendizado apoio reconhecimento clareza oportunidade
    ]

    NEGATIVE = %w[
      ruim péssimo insatisfeito frustrado desmotivado confuso pressão
      falta problema cobrança desorganizado
    ]
  end
end
